package ISC;

public class Day86 {

    public static void main(String[] args) {
        // Mencetak elemen pada Array satu dimensi
        int[] a = {1, 2, 3, 4, 5};
        System.out.println("Elemen array: ");
        for (int i = 0; i < a.length; i++) {
            System.out.println("Index " + i + " = " + a[i]);
        }
    }
}
